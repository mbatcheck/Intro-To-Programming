# Create a Hash using both Ruby syntax styles.

# hash syntax 1
my_family = {:son => '30', :daughter => '28', :mother => '52', :father => '55'}
puts "this is my family: #{my_family}"

# hash syntax 2
my_family = {son: '30', daughter: '28', mother: '52', father: '55'}
puts "this is my family: #{my_family}"
